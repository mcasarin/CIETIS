      <h1 class="display-4">Sobre o CIETIS</h1>
      <p>O Colóquio Internacional de Educação Interprofissional em Saúde (CIETIS) é o principal evento da ReBETIS. Desde 2015, suas cinco edições abordaram importantes temáticas da interprofissionalidade por meio de ricos debates e compartilhamento de experiências de seus participantes e de convidados nacionais e internacionais.</p>
      <p>Este ano, o 6º CIETIS será sediado na região Norte do Brasil em <strong>formato on-line</strong> no período de 25 a 28 de abril de 2023. “A centralidade do usuário para sustentabilidade da interprofissionalidade na formação e no trabalho em saúde” é o tema que estimulará reflexões e discussões acerca da importância de garantir o lugar central de indivíduos, famílias e comunidades no processo de cuidado em saúde e de como esse resgate é essencial para a reorientação da formação e das práticas de saúde na perspectiva interprofissional de modo sustentável.</p>
      <p>Nesse sentido, os usuários dos serviços de saúde ocuparão lugares privilegiados no 6º CIETIS, sendo convidados a participar das <strong>Malocas</strong>, compartilhando suas visões, experiências e expectativas juntamente com os demais convidados, mediadores e debatedores. Nas Malocas, acontecerão mesas redondas, conferências e plenárias, seguidas de debate com todos os participantes do evento.</p>
      <p>O compartilhamento de saberes e experiências dos participantes também tem se tornado importante atividade de todos os CIETIS, em virtude da necessidade de conhecermos, refletirmos e contribuirmos com as iniciativas brasileiras. Este ano, além de resultados de pesquisa e de relatos de experiências, será possível apresentar produções artístico-culturais relacionadas à interprofissionalidade. As <strong>Pororocas</strong>, assim denominadas essas sessões, ocorrerão em três dos quatro dias de evento. Pororoca é o fenômeno natural resultante do encontro das águas do Rio Amazonas com as águas do mar. Tal fenômeno provoca estrondoso barulho, altas ondas e terras caídas.</p>
      <p>A cultura do Norte será semeada durante todo o 6º CIETIS por <strong>Sementes de Cultura</strong>, que consistem em vídeos cuidadosamente selecionados para apresentar manifestações artístico-culturais dos sete estados que compõem a região. Espera-se que as sementes brotem e enraízem fortalecendo o evento.</p>
      <p>Considerando o grande tempo de visualização de tela e outras peculiaridades na vida contemporânea, as <strong>Sessões de Bicho-preguiça</strong> proporcionarão práticas de autocuidado e cuidado coletivo dos participantes. Serão conduzidas por profissionais de saúde e artistas convidados, estarão inseridas estrategicamente na programação e poderão ser realizadas por todos os participantes onde estiverem.</p>
      <p>O último dia de evento será reservado para os compromissos finais como aprovação da Carta do Norte (documento final do evento) e aprovação do Regimento da ReBETIS, recomposição Executiva Nacional e escolha da sede do próximo CIETIS.</p>
      <p>De uma forma muito especial, o 6º CIETIS homenageará Clélia Parreira, educadora, notável docente da Faculdade de Ceilândia - Universidade de Brasília, membro do Grupo Consultivo da ReBETIS, participante da ReBETIS desde sua fundação, entusiasta e liderança da educação interprofissional em saúde no Brasil.</p>
